class University:
    departments = ["Biotech", "Chemistry", "Engineering", "Mathematics", "Physics"]
    dpt_finals_position = {"Physics": 2, "Chemistry": 3, "Mathematics": 4, "Engineering": 5, "Biotech": 3}

    def __init__(self, accept_num):
        self.accept_num = accept_num
        self.applicants_total = []
        self.applicants = {}
        self.departments_students = {}
        self.accepted = []

    def get_total_applicants(self):
        with open("applicants.txt", "r") as applicants_file:
            self.applicants_total = [apt.strip("\n").split() for apt in applicants_file.readlines()]
        # self.applicants_total = [apt.strip("\n").split() for apt in application_list.split("\n")]
        # print(self.accepted)

    def get_applicants_list(self, rank):
        self.applicants = {}
        for apt in self.applicants_total:
            self.applicants.setdefault(apt[rank + 6], []).append(apt)
        # print(self.applicants)
        for dpt in self.applicants:
            i = self.dpt_finals_position[dpt]
            self.applicants[dpt].sort(key=lambda x: (-float(x[i]), x[0], x[1]))

    def accept_students(self):
        for dpt in self.applicants:
            for apt in self.applicants[dpt]:
                if not self.departments_students.get(dpt) or len(self.departments_students[dpt]) < self.accept_num:
                    self.departments_students.setdefault(dpt, []).append(apt)
                    self.accepted.append(apt)
        for x in self.accepted:
            if x in self.applicants_total:
                self.applicants_total.remove(x)

    def display_admission(self):
        for dpt in self.departments:
            i = self.dpt_finals_position[dpt]
            self.departments_students[dpt].sort(key=lambda x: (-float(x[i]), x[0], x[1]))
            print(dpt)
            for student in self.departments_students[dpt]:
                print(f"{student[0]} {student[1]} {student[i]}")


